#include <bits/stdc++.h>

using namespace std;

int main(int argc, char *argv[]) {

  unsigned short int usi;
  signed short int ssi;
  unsigned int ui;
  signed int si;
  unsigned long int uli;
  signed long int sli;
  unsigned long long int ulli;
  signed long long int slli;

  cout<< "Unsigned Short Int Size : " << sizeof(usi) << endl;
  cout<< "Signed Short Int Size : " << sizeof(ssi) << endl;
  cout<< "Unsigned Int Size : " << sizeof(ui) << endl;
  cout<< "Signed Int Size : " << sizeof(si) << endl;
  cout<< "Unsigned Long Int Size : " << sizeof(uli) << endl;
  cout<< "Signed Long Int Size : " << sizeof(sli) << endl;
  cout<< "Unsigned Long Long Int Size : " << sizeof(ulli) << endl;
  cout<< "Signed Long Long Int Size : " << sizeof(slli) << endl;
  return 0;
}
